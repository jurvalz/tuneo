package com.tuneo.ws.tuneows.model;

public class Favorito {

    private Integer coFavorito;
    private Integer coProducto;
    private String fecha;

    public Integer getCoFavorito() {
        return coFavorito;
    }

    public void setCoFavorito(Integer coFavorito) {
        this.coFavorito = coFavorito;
    }

    public Integer getCoProducto() {
        return coProducto;
    }

    public void setCoProducto(Integer coProducto) {
        this.coProducto = coProducto;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}
